static int cmp_nick(const void *v1, const void *v2)
{
    return strcmp((const char *)v1, ((struct nv2_players *)v2)->nick);
}

int get_player_id(const char *nick)
{
    struct nv2_players *i;

    i = bsearch(nick, nv2_players, sizeof nv2_players/ sizeof *nv2_players, sizeof *nv2_players, cmp_nick);
    return i ? i->id : -1;
}
